	            <h1>Advice on Choosing a Hearing Aid Dispenser</h1>
	            <h2>Advice from Maggie Dodd, President of the Canadian Hard of Hearing Association (CHHA) Chilliwack Branch</h2>
	            <p>Many potential hearing-aid users are completely at a loss when shopping for their first instruments. A family physician may have referred a patient to an ear-nose-and-throat doctor to determine if a person is an appropriate candidate to be fitted with hearing aids or if there is some medical cause which can be treated by medication, surgery or some other treatment. This doctor may have in turn suggested that the patient visit the hearing aid dispenser of choice to be fitted.</p>
	            <p>A patient then makes an appointment to consult a hearing aid dispenser. This person should have been qualified by the BC College of Speech and Hearing Practitioners.</p>
	            <p>This dispenser should conduct a thorough assessment of the patient's hearing and may offer suggestions as to the type and style of hearing aid. If the patient does not understand what is being offered, that person should question the dispenser until all questions have been answered to the patient's satisfaction. </p>
	            <p>It is perfectly acceptable to consult another dispenser if the consumer does not feel satisfied that all the questions have been answed or the patient feels that these questions have been brushed off. Each hearing aid company has different business practices when supplying instruments. Consumers should make sure that they understand these business practices before making any commitments.</p>
	            <p><em>Maggie Dodd is President of the <a href="http://chha.ca/chha/">Canadian Hard of Hearing Association</a> (CHHA) Chilliwack Branch and presenter of the Managing Hearing Loss program with classes at Elder College, University of the Fraser Valley. Maggie has been associated with CHHA since the early '90s and became a qualified Speechreading and Managing Hearing Loss instructor through CHHA and the Deaf and Hard of Hearing Centre in Calgary. She has conducted courses in many parts of the province with the last course of 12 people at UFV in Chilliwack.</em></p>
	            <p><em>The Chilliwack Resource Centre of the CHHA has offices  at #102, 9300 Nowell Street, Chilliwack, 604-795-9238 or toll-free 1-866-888-2442.</em></p>
