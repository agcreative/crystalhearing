            <h1>Frequently Asked Questions Regarding Hearing &amp; Hearing Loss</h1>
	            <h2>Why should I correct a hearing loss?</h2>
	            <p>Hearing is essential to your quality of life. Helen Keller said that loss of vision means losing contact with things, but loss of hearing means losing contact with people. A hearing loss influences the interaction between family and friends causing communication barriers between people who love and need one another. Anxiety, insecurity, isolation, and depression can affect those who do not hear well. </p>
	            <p>Scientific studies indicate that the negative psychological changes due to untreated hearing loss may be reversed by the use of hearing aids. You will not only improve your own quality of life. By being able to communicate and participate in active conversation, you will also improve the quality of life of your loved ones, who will now be able and more willing to fully interact with you once again.</p>
	            <h2>I've heard the terms &quot;Permanent Distortion&quot; and &quot;Phonemic Regression&quot; numerous times. What exactly do they mean?</h2>
	            <p>To most people, a &quot;hearing problem&quot; means just one thing: hearing loss, where the words simply become fainter. Well, it's not that simple. Distortion plays a significant role in hearing problems. It starts almost unnoticed. &quot;Effs&quot; begin to sound like &quot;esses&quot;, &quot;dees&quot; like &quot;tees&quot;, the word &quot;cat&quot; may be confused with &quot;hat&quot;, etc. </p>
	            <p>At first it doesn't seem to matter. You can still make out the words by hearing the context of the complete sentence. But little by little, before long, only half the words are clear... and many you may not even be able to make out at all. This is distortion. And the truth is, even if words are made loud enough with the correct hearing aids, inner ear distortion may continue to make them sound garbled. This is referred to as &quot;Phonemic Regression&quot;.</p>
	            <p>Although in most cases it takes many years, eventually this distortion may become permanent &ndash; ruining your ability to understand some words at all. Thus the term Permanent Distortion. That's why it's so important to see your hearing healthcare professional as soon as a hearing loss is noticed.</p>
	            <h2>If it's so important to get help, why do people refuse help for hearing loss?</h2>
	            <p>Denial, or minimizing a hearing loss, frequently occurs. People incorrectly associate it with aging and disability. They simply ask people to talk louder or repeat themselves. Some are apprehensive about the challenge of learning how to use and insert a sophisticated, miniature electronic device. Others may feel shame and embarrassment about wearing a hearing instrument, and will put it off until the distortion has grown so much that they can't understand some conversations at all. </p>
	            <p>With today's micro-processor technology, it's truly tragic when some people wait too long to get help, and put it off until it's too late to be able to hear clearly &ndash; even with the best hearing aids available. When hearing impaired individuals accept the advantages of hearing aids, they are amazed at how much they were unknowingly missing in life.</p>
